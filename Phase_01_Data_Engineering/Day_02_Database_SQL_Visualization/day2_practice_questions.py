query_q5 = """
SELECT department,
       ROUND(AVG(programming_score),2) AS avg_programming
FROM students
GROUP BY department
ORDER BY avg_programming DESC;
"""

chart_q5 = pd.read_sql_query(query_q5, conn)


fig, ax = plt.subplots(figsize=(9,5))


colors = ['skyblue', 'orange', 'limegreen', 'violet']


bars = ax.bar(
    chart_q5['department'],
    chart_q5['avg_programming'],
    color=colors,
    edgecolor='black',
    linewidth=2
)


ax.bar_label(
    bars,
    fmt='%.1f',
    padding=3,
    fontsize=10,
    fontweight='bold'
)


ax.set_title(
    "Average Programming Score by Department",
    fontsize=15,
    fontweight='bold'
)


ax.set_xlabel(
    "Department",
    fontsize=12,
    fontweight='bold'
)

ax.set_ylabel(
    "Average Programming Score",
    fontsize=12,
    fontweight='bold'
)


ax.grid(
    axis='y',
    linestyle='--',
    alpha=0.4
)


ax.set_facecolor('#f5f5f5')


plt.xticks(rotation=10)

plt.show()

