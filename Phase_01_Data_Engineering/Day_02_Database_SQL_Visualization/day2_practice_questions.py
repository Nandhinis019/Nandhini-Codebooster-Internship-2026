query1 = """
SELECT department,
       ROUND(AVG(math_score),2) AS avg_math
FROM students
GROUP BY department
ORDER BY avg_math DESC;
"""

chart1_data = pd.read_sql_query(query1, conn)

print(chart1_data)

fig, ax = plt.subplots(figsize=(8,5))

bars = ax.bar(
    chart1_data['department'],
    chart1_data['avg_math'],
    color=['skyblue','orange','green','purple']
)

