import os
from getpass import getpass

# LangChain Imports
from langchain_groq import ChatGroq
from langchain_core.prompts import PromptTemplate
from langchain_core.output_parsers import StrOutputParser

# Twilio Import
from twilio.rest import Client

# ======================================================
# STEP 1: GROQ API KEY
# ======================================================

if "GROQ_API_KEY" not in os.environ:
    os.environ["GROQ_API_KEY"] = getpass("Enter GROQ API Key: ")

# ======================================================
# STEP 2: LLM
# ======================================================

llm = ChatGroq(
    model="llama-3.3-70b-versatile",
    temperature=0
)

# ======================================================
# STEP 3: TREE OF THOUGHTS PROMPT
# ======================================================

prompt = PromptTemplate(
    input_variables=["question"],
    template="""
You are an advanced reasoning assistant.

Step 1: Generate THREE different reasoning paths to solve the problem.

Step 2: Evaluate each path for correctness.

Step 3: Choose the best path.

Step 4: Provide the final answer.

Question: {question}

Return the answer in this format:

Reasoning Path 1:

Reasoning Path 2:

Reasoning Path 3:

Evaluation:

Best Path:

Final Answer:
"""
)

# ======================================================
# STEP 4: OUTPUT PARSER
# ======================================================

