# ======================================================
# STEP 5: CHAIN
# ======================================================

chain = prompt | llm | parser

# ======================================================
# STEP 6: USER QUESTION
# ======================================================

question = input("Enter your question/problem: ")

response = chain.invoke({
    "question": question
})

