from langchain_groq import ChatGroq
from langchain_core.prompts import PromptTemplate
from langchain_core.output_parsers import StrOutputParser

# Twilio Import
from twilio.rest import Client

# -----------------------------------
# GROQ API KEY
# -----------------------------------

if "GROQ_API_KEY" not in os.environ:
    os.environ["GROQ_API_KEY"] = getpass("Enter GROQ API Key: ")

# -----------------------------------
# LLM
# -----------------------------------

llm = ChatGroq(
    model="llama-3.3-70b-versatile",
    temperature=0
)

# -----------------------------------
# PROMPT TEMPLATE
# -----------------------------------

prompt = PromptTemplate(
    input_variables=["problem"],
    template="""
You are an expert competitive programmer.

For the given problem:

1. Generate 4 approaches.
2. Compare Time and Space Complexity.
3. Select the best approach.
4. Generate optimized Python code.
5. Include sample input and output.

Problem:
{problem}
"""
)

# -----------------------------------
# PARSER
# -----------------------------------

parser = StrOutputParser()

# -----------------------------------
# CHAIN
# -----------------------------------

chain = prompt | llm | parser

# -----------------------------------
# USER INPUT
# -----------------------------------

problem = input("Enter Coding Problem: ")

response = chain.invoke({
    "problem": problem
})

print("\n========================")
print("AI GENERATED SOLUTION")
