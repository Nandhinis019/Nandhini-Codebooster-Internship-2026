# Convert Tanglish/Tamil-English query into proper English
def normalize_question(user_question, client, model):
    prompt = f"""
You are a translator for database queries.

Convert the following user query into clear English.

Examples:
"female students ah kaatu" -> "Show all female students"
"male students count sollu" -> "Count all male students"
"computer science la iruka students" -> "Show all students in Computer Science department"
"CS department students count" -> "Count students in Computer Science department"

User Query:
{user_question}

Return ONLY the English query.
"""

    response = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "user", "content": prompt}
        ],
        temperature=0
    )

    return response.choices[0].message.content.strip()


