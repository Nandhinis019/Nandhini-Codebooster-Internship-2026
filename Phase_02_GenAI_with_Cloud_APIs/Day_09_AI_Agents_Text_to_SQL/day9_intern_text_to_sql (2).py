import os
import gradio as gr
from langchain_groq import ChatGroq
from langchain_core.prompts import PromptTemplate
from langchain_core.output_parsers import StrOutputParser

def generate_captions(
    groq_api_key,
    platform,
    content_type,
    tone,
    description,
    length,
    hashtags,
    emoji,
    num_captions,
    max_tokens
):
    try:
        # Set API Key
        os.environ["GROQ_API_KEY"] = ""

        # LLM
        llm = ChatGroq(
            model="llama-3.3-70b-versatile",
            temperature=0.7,
            max_tokens=int(max_tokens)
        )

        # Prompt Template
        prompt = PromptTemplate(
            input_variables=[
                "platform",
                "content_type",
                "tone",
                "description",
                "length",
                "hashtags",
                "emoji",
                "num_captions"
            ],
            template="""
You are an expert social media content creator.

Generate {num_captions} DIFFERENT caption options.

User Preferences:
Platform: {platform}
Content Type: {content_type}
Tone: {tone}
Description: {description}
Caption Length: {length}
Include Hashtags: {hashtags}
Include Emojis: {emoji}

Platform Guidelines:
- Instagram → trendy, engaging
- LinkedIn → professional
- WhatsApp → short and impactful
- Facebook → conversational
- X/Twitter → concise and attention-grabbing
- GitHub → technical and developer-focused

Requirements:
1. Every caption must be different.
2. Use the requested tone.
3. Follow the requested length.
4. Include emojis only if requested.
5. Include hashtags only if requested.
6. Number each caption clearly.
7. Make captions creative and high quality.

Output Format:

Caption 1:
...

Caption 2:
...

Caption 3:
...
"""
        )

        parser = StrOutputParser()

        chain = prompt | llm | parser

        response = chain.invoke({
            "platform": platform,
            "content_type": content_type,
            "tone": tone,
            "description": description,
            "length": length,
            "hashtags": hashtags,
            "emoji": emoji,
            "num_captions": num_captions
        })

        return response

    except Exception as e:
        return f"Error: {str(e)}"


